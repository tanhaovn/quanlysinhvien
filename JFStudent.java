    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String id = this.txtID.getText().trim();
        String name = this.txtName.getText().trim();
        String ageText = this.txtAge.getText().trim();

        // Kiểm tra điều kiện nhập liệu không được để trống
        if (id.isEmpty() || name.isEmpty() || ageText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!");
            return;
        }

        int age;
        try {
            age = Integer.parseInt(ageText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Tuổi phải là một số nguyên hợp lệ!");
            return;
        }

        // Kiểm tra điều kiện của tuổi
        if (age <= 0) {
            JOptionPane.showMessageDialog(this, "Tuổi phải lớn hơn 0!");
            return;
        }

        // Kiểm tra trùng ID chỉ khi thêm mới (check == 1)
        if (check == 1) {
            for (Student student : List) {
                if (student.getId().equalsIgnoreCase(id)) {
                    JOptionPane.showMessageDialog(this, "MSSV đã tồn tại, vui lòng nhập MSSV khác!");
                    return;
                }
            }
            List.add(new Student(id, name, age));
        } else if (check == -1) {
            for (int i = 0;i<List.size();i++){
                if (i != pos && List.get(i).getId().equalsIgnoreCase(id)) {
                    JOptionPane.showMessageDialog(this, "MSSV đã tồn tại, vui lòng nhập MSSV khác!");
                    return;
                }
            }
            List.set(pos, new Student(id, name, age));
        }
    View();  
    ViewTable();  
    JOptionPane.showMessageDialog(this, "Lưu thành công!");

    // Xóa dữ liệu sau khi lưu để dễ nhập liệu tiếp
    this.txtID.setText("");
    this.txtName.setText("");
    this.txtAge.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

