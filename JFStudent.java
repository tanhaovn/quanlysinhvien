    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (pos < 0 || pos >= List.size()){
            JOptionPane.showMessageDialog(this,"Không có sinh viên để xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sinh viên này không?","Xác nhận xóa",JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION){
            List.remove(pos);
            if (pos >= List.size()) {
            pos = List.size() - 1;  // Đảm bảo pos không vượt quá giới hạn
            }

            View();
            ViewTable();
            JOptionPane.showMessageDialog(this,"Xóa thành công!");
        }else{
            JOptionPane.showMessageDialog(this,"Hủy xóa!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

